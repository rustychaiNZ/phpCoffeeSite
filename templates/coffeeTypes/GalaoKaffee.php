			<p>Galão ([ɡaˈlɐ̃õ]) is a hot drink from Portugal made of espresso and foamed milk. Similar to caffè latte or café au lait, it consists of about one quarter coffee and three quarters foamed milk. It is served in a tall glass, as opposed to the smaller garoto that is served in a demitasse. When the proportion is 1:1 it is called meia de leite (half of milk) and it comes in a cup.</p>
